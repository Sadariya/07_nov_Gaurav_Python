# 17_Write a Python program to split a list into different variables.

# static list :- 
list = [[12,11],[13,14],[10,6]]

# Assign list to three variable :- 
var1,var2,var3 = list

# print the list into three different variable :- 
print (f"1 : {var1}")
print (f"2 : {var2}")
print (f"3 : {var3}")